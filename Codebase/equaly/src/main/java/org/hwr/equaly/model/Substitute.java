package org.hwr.equaly.model;

public class Substitute {
    private String word = "";
    private String gender = "";
    private String fall = "";
    private String numerus = "";
    private String oldGender = "";
    private String oldFall = "";
    private String oldNumerus = "";

    public String getOldGender() {
        return oldGender;
    }

    public void setOldGender(String oldGender) {
        this.oldGender = oldGender;
    }

    public String getOldFall() {
        return oldFall;
    }

    public void setOldFall(String oldFall) {
        this.oldFall = oldFall;
    }

    public String getOldNumerus() {
        return oldNumerus;
    }

    public void setOldNumerus(String oldNumerus) {
        this.oldNumerus = oldNumerus;
    }

    public String getWord() {
        return word;
    }

    public void setWord(String word) {
        this.word = word;
    }

    public String getNumerus() {
        return numerus;
    }

    public void setNumerus(String numerus) {
        this.numerus = numerus;
    }

    public String getGender() {
        return gender;
    }

    public void setGender(String gender) {
        this.gender = gender;
    }

    public String getFall() {
        return fall;
    }

    public void setFall(String fall) {
        this.fall = fall;
    }
}

